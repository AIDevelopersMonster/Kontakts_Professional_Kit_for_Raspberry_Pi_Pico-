import time  # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –º–æ–¥—É–ª—å –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –∑–∞–¥–µ—Ä–∂–∫–∞–º–∏
import array  # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –º–æ–¥—É–ª—å –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –º–∞—Å—Å–∏–≤–∞–º–∏
import math  # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –º–æ–¥—É–ª—å –¥–ª—è –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏—Ö –≤—ã—á–∏—Å–ª–µ–Ω–∏–π
import board  # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –º–æ–¥—É–ª—å –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –ø–∏–Ω–∞–º–∏ –º–∏–∫—Ä–æ–∫–æ–Ω—Ç—Ä–æ–ª–ª–µ—Ä–∞
import audiobusio  # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –º–æ–¥—É–ª—å –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –∞—É–¥–∏–æ–¥–∞—Ç—á–∏–∫–∞–º–∏ —á–µ—Ä–µ–∑ PDM

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã—á–∏—Å–ª–µ–Ω–∏—è —Å—Ä–µ–¥–Ω–µ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è –º–∞—Å—Å–∏–≤–∞ (–∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è —Å–º–µ—â–µ–Ω–∏—è DC)
def mean(values):
    """
    –í—ã—á–∏—Å–ª—è–µ—Ç —Å—Ä–µ–¥–Ω–µ–µ –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–æ–µ —Å–ø–∏—Å–∫–∞ –∑–Ω–∞—á–µ–Ω–∏–π.

    –ê—Ä–≥—É–º–µ–Ω—Ç—ã:
    values - —Å–ø–∏—Å–æ–∫ —á–∏—Å–ª–æ–≤—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π

    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç:
    –°—Ä–µ–¥–Ω–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ —Å–ø–∏—Å–∫–∞
    """
    return sum(values) / len(values)

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –Ω–æ—Ä–º–∞–ª–∏–∑–æ–≤–∞–Ω–Ω–æ–≥–æ RMS (Root Mean Square - —Å—Ä–µ–¥–Ω–µ–∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ)
def normalized_rms(values):
    """
    –í—ã—á–∏—Å–ª—è–µ—Ç –Ω–æ—Ä–º–∞–ª–∏–∑–æ–≤–∞–Ω–Ω–æ–µ —Å—Ä–µ–¥–Ω–µ–∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ (RMS).

    –ê—Ä–≥—É–º–µ–Ω—Ç—ã:
    values - –º–∞—Å—Å–∏–≤ –∞—É–¥–∏–æ—Å–µ–º–ø–ª–æ–≤

    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç:
    RMS —É—Ä–æ–≤–µ–Ω—å –∞—É–¥–∏–æ—Å–∏–≥–Ω–∞–ª–∞
    """
    minbuf = int(mean(values))  # –í—ã—á–∏—Å–ª—è–µ–º —Å—Ä–µ–¥–Ω–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è DC-—Å–º–µ—â–µ–Ω–∏—è
    samples_sum = sum(
        float(sample - minbuf) * (sample - minbuf) for sample in values)  # –í—ã—á–∏—Å–ª—è–µ–º —Å—É–º–º—É –∫–≤–∞–¥—Ä–∞—Ç–æ–≤ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–π

    return math.sqrt(samples_sum / len(values))  # –ò–∑–≤–ª–µ–∫–∞–µ–º –∫–≤–∞–¥—Ä–∞—Ç–Ω—ã–π –∫–æ—Ä–µ–Ω—å –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è RMS

# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è PDM-–º–∏–∫—Ä–æ—Ñ–æ–Ω–∞
mic = audiobusio.PDMIn(
    board.GP9, board.GP8,  # GP9 - Data (DOUT), GP8 - Clock (CLK)
    sample_rate=16000,  # –ß–∞—Å—Ç–æ—Ç–∞ –¥–∏—Å–∫—Ä–µ—Ç–∏–∑–∞—Ü–∏–∏ 16 –∫–ì—Ü
    bit_depth=16  # –ì–ª—É–±–∏–Ω–∞ –±–∏—Ç–Ω–æ—Å—Ç–∏ 16 –±–∏—Ç
)

# –°–æ–∑–¥–∞–µ–º –º–∞—Å—Å–∏–≤ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∞—É–¥–∏–æ—Å–µ–º–ø–ª–æ–≤ (160 –∑–Ω–∞—á–µ–Ω–∏–π —Ç–∏–ø–∞ 'H' - 16-–±–∏—Ç–Ω—ã–µ —á–∏—Å–ª–∞)
samples = array.array('H', [0] * 160)

# –û—Å–Ω–æ–≤–Ω–æ–π —Ü–∏–∫–ª –¥–ª—è –∑–∞–ø–∏—Å–∏ –∏ –∞–Ω–∞–ª–∏–∑–∞ –∞—É–¥–∏–æ—Å–∏–≥–Ω–∞–ª–∞
while True:
    mic.record(samples, len(samples))  # –ó–∞–ø–∏—Å—ã–≤–∞–µ–º 160 —Å—ç–º–ø–ª–æ–≤ —Å –º–∏–∫—Ä–æ—Ñ–æ–Ω–∞
    magnitude = normalized_rms(samples)  # –í—ã—á–∏—Å–ª—è–µ–º RMS —É—Ä–æ–≤–µ–Ω—å —Å–∏–≥–Ω–∞–ª–∞
    print((magnitude,))  # –í—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤ –∫–æ–Ω—Å–æ–ª—å (–¥–ª—è –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –≤ Plotter –≤ Mu Editor)
    time.sleep(0.1)  # –î–µ–ª–∞–µ–º –ø–∞—É–∑—É –≤ 100 –º—Å –ø–µ—Ä–µ–¥ —Å–ª–µ–¥—É—é—â–µ–π –∑–∞–ø–∏—Å—å—é

