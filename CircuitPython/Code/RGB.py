# –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Ü–≤–µ—Ç–∞ –≤ —Ñ–æ—Ä–º–∞—Ç–µ RGB (Red, Green, Blue)
R = (50, 0, 0)  # –ö—Ä–∞—Å–Ω—ã–π —Ü–≤–µ—Ç —Å —è—Ä–∫–æ—Å—Ç—å—é 50
